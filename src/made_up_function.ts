/**
 * This is a test function for Jest, for validating the Jest setup.
 *
 * @param a - The first number.
 * @param b - The second number.
 * @param c - The third number.
 * @returns The sum of a, b, and c.
 */
function testTheTestSetup(a: number, b: number, c: number): number {
  const result = a + b + c;
  return result;
}
export { testTheTestSetup };
